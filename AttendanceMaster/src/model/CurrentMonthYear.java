package model;

public class CurrentMonthYear {
	int month;
	int year;
	
	public int getMonth() {
		return month;
	}
	public int getYear() {
		return year;
	}
	public void setMonth(int month) {
		this.month = month;
	}
	public void setYear(int year) {
		this.year = year;
	}
	

}
